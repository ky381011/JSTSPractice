// 通常の加算
let num0 = 20;
num0 = num0 + 5;
console.log(num0);

// 複合演算子による加算
let num1 = 20;
num1 += 5;
console.log(num1);

// 複合演算子による減算
let num2 = 20;
num2 -= 5;
console.log(num2);

// 複合演算子による乗算
let num3 = 20;
num3 *= 5;
console.log(num3);

// 複合演算子による除算
let num4 = 20;
num4 /= 5;
console.log(num4);
